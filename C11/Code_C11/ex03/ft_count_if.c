int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int		i;
	int		size;

	i = 0;
	size = 0;
	while (i < length)
	{
		if (f(tab[i]) != 0)
			size++;
		i++;
	}
	return (size);
}
